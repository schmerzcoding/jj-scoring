export function getCountryName(code: string | null | undefined): string {
  if (!code) return "";
  return COUNTRIES.find((country) => country.code === code)?.name ?? code;
}

export function countrySelectOptions(includeEmpty = true) {
  const options = COUNTRIES.map((country) => ({
    value: country.code,
    label: country.name,
  }));

  if (includeEmpty) {
    return [{ value: "", label: "Select country" }, ...options];
  }

  return options;
}
